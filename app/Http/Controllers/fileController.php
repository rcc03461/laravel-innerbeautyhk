    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $file = $request->file('file');

        $path = $file->storeAs(
            'public',
            time() . '.' . $file->extension()
        );
        File::create([
            "name" => $file->getClientOriginalName(),
            "desc" => "",
            "size" => $file->getSize(),
            "ext" => $file->extension(),
            "path" => $path
        ]);
    }

